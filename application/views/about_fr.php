		<h3>Qui est derrière ?</h3>
		La Machine à données est un projet du ministère de l’Enseignement supérieur, de la Recherche et de l’Innovation développé dans le cadre de la première édition (2017) du programme Entrepreneurs d’intérêt général. Léo Bouloc et Laurent Dupont, entrepreneurs d’intérêt général, et Jérémy Péglion (Département des outils d’aide à la décision) ont pensé, conçu et développé ce service. Yann Caradec et Emmanuel Weisenburger (Département des outils d’aide à la décision) ont assuré le mentorat du projet.

